# Section 3: Discovery

## Introduction

The tension in discovery comes from buyer skepticism: "Does this rep know anything about me?" and "Are they going to push me to buy, even if it''s not a fit?"

### Discovery Pitfalls

**Pitfall #1: Setting an agenda, not an objective**
Move from setting an agenda to setting an objective: "By the end of the call today, I will be able to tell you specifically how our solution will help you drive [relevant positive business outcomes]. To get to that point, I will need to understand your role as it relates to [relevant current state]."

**Pitfall #2: "Tell me about your role"**
This question is too broad and signals you''re unprepared. Instead, get specific: "Tell me about your role as it relates to [relevant positive business outcomes]."

