# Section 2: Pipeline Generation

## Introduction

If you are dependent on other sources for your quota success, guess who loses their sales job as soon as those sources slow down? If you aspire to be a differentiated and valuable seller, there''s nothing that will set you apart like an ability to source, progress, and close high-quality deals.

## Identify and Understand Prospects

Just as you need to understand the types of companies most likely to have a strong use case, you need to understand the contacts/titles within those accounts most likely to benefit.

### Understand Priorities of Key Contacts

A big mistake companies make when onboarding new sellers is starting with product training. Understanding your prospects'' problems and priorities is much more important than knowing your product.

Read job descriptions for open roles in the same department as your target prospect. Organize information in a "Buyer Persona Matrix":
- What individuals care about (business outcomes)
- How individuals spend their time
- What individuals often struggle with
- How your solution may help

## Point of View

"Have a strong opinion, loosely held." I''d rather my team show up with a slightly wrong point of view than no point of view at all. Your point of view should be built from your knowledge of the account and of the prospect.

## Messaging: Emotional Relevance Framework

This framework is channel-agnostic: applicable for cold email, cold calls, inMails, or any other outreach.

The Emotional Relevance Framework:
1. Drive emotion by connecting to their aspirations and concerns
2. Show you understand their current role/day-to-day
3. Clearly explain your proposed after state - don''t leave them wondering "what do you actually do?"
4. Provide evidence that you can actually back up your claims

### Messaging - Specificity (Competitor''s Website Test)

Can you find the exact value proposition you used in your email on your competitor''s website? If so, it''s too generic.

Leading with value isn''t enough to get attention anymore. Instead, lead with **differentiated and defensible value**.

Three questions your messaging must answer:
- "How are you different?"
- "Why does it matter?"
- "How can you prove it?"

## Prospecting Cadence: Good Day Framework

### Metrics to Track (target: 8 points per day):

- **Prospects Added to Cadence:** 1 point per 10 contacts added
- **Prospecting Calls with Conversations:** 1 point each
- **Custom inMails/Emails to Manager+ contacts:** 1 point per 5 messages
- **Initial Meetings Set:** 1 point each
- **Opportunities Created:** 1 point each
- **Personal Development:** 1 point for an hour (max 1/day)

## Objection Handling

Objections aren''t meant to be handled, they are meant to be understood and addressed.

When you get a "no," don''t fall back to the high-level outcome. They likely either:
- Don''t believe you can drive that outcome
- Believe a different approach will work better

You need to find out which one and address that specifically.

## Pipeline Generation Diagnosis

Three-step diagnosis when you hit a slow period:

1. **Are you calling the right accounts?** Check for potential budget and use case.
2. **Are you calling the right contacts?** Would this person personally benefit? Are they responsible for solving the business pain?
3. **Is your messaging effective?** Check for emotional relevance and specificity.

