# Section 1: Territory Planning

## Introduction

There has been one constant in my career: an obsession with effective territory management. As an AE, I spent hours manually scoring thousands of accounts so I could focus my time where I was most likely to win. As a VP, I spend hours identifying signals and trends to ensure I set up all of my team for success.

The objective of this first section is to teach you the foundational elements of territory management. After review, you''ll be equipped to spend more time selling to accounts where you''ll win, and less time wondering, "which accounts should I work today?"

## Account Scoring

Considering that a sales rep''s assigned territory is how they make money, it''s surprising how rarely AEs understand where they should spend their time. When a new AE (or leader) joins my organization, priority number one is helping them understand how to organize, understand, and optimize their patch for sustained success.

When scoring accounts, most reps place too much weight on revenue and employee count. They default to, "the bigger the account, the bigger the opportunity." Heavy reliance on any of these three metrics is ineffective for two reasons:

1. Data quality (especially for privately held companies) is usually garbage
2. These quantitative measures don''t tell the whole story

### Prioritization Framework: Use Case and Budget

**Use Case:** You can identify where and how your product would be used.
**Budget:** The company likely invests, or would invest, in solutions like yours.

### Account Tiering

- **Priority 1** - Use Case AND Budget
- **Priority 2** - Use Case OR Budget
- **Priority 3** - No Use Case or Budget

Plan to spend 80%+ of your time on Priority 1 accounts. Use more automated approaches + SDR support on Priority 2, and never look at Priority 3 accounts again.

### Budget Signals

**Competitive solution usage:** Search current employees'' LinkedIn profiles for competitor names and look at open job descriptions. If they are spending on a competitor, there''s potential budget for you to capture.

**Headcount growth in target departments:** If they are investing in headcount, they''ll invest in solutions to support that headcount. Check open job postings and LinkedIn Insights tab.

## Understanding Business Models

Now that you are focusing on the right accounts, optimize your approach by ensuring you have a strong understanding of their business models:

- How do they make money?
- How do they interact with their customers?
- What is the current health of the business?

Ask yourself: "If you had a meeting with their executive team, how comfortable are you discussing their business in their language and tying your solution to their goals?"

## Customer Expansion

Most AEs delay expansion conversations until the last months of the license. This kills their chance at upsell, because renewal budgets are planned well in advance.

Framework for upgrading customers:
1. List the problems you are currently solving for the customer
2. Identify the problems you should be solving that you are not yet addressing
3. Identify who is likely responsible for solving the problems you should be solving

